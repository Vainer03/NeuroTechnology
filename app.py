        st.button("–û–±—Ä–∞–±–æ—Ç–∞—Ç—å –∏ –¥–æ–±–∞–≤–∏—Ç—å —Å—É–±—Ç–∏—Ç—Ä—ã", key="process_button_disabled", disabled=True)
